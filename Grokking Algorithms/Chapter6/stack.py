stack = []

# .append agrega un elemento al stack
stack.append('a')
stack.append('b')
stack.append('c')

print('Stack inicial')
print(stack)

# .pop() remueve elementos del stack
print('\nElementos removidos del stack:')
print(stack.pop())
print(stack.pop())
print(stack.pop())

print('\nStack despues de remover los elementos:')
print(stack)

